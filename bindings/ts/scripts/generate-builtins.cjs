#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

const bindingsRoot = path.resolve(__dirname, "..");
const builtinsDir = path.resolve(
  bindingsRoot,
  "../../crates/runmat-runtime/src/builtins/builtins-json"
);
const generatedRoot = path.resolve(bindingsRoot, "src/generated");
const generatedBuiltinsDir = path.resolve(generatedRoot, "builtins");
const manifestPath = path.resolve(generatedRoot, "builtins-manifest.ts");
const examplesCatalogPath = path.resolve(generatedRoot, "builtin-examples-catalog.ts");

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function readBuiltinSourceFiles() {
  return fs
    .readdirSync(builtinsDir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith(".json"))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

function normalizeBuiltinKey(value) {
  return String(value).trim().toLowerCase();
}

function slugFromTitle(title) {
  return String(title).trim().toLowerCase();
}

function categoryPathFromCategory(category) {
  if (typeof category !== "string") {
    return [];
  }
  return category
    .split("/")
    .map((part) => part.trim())
    .filter(Boolean);
}

function formatObjectLiteral(value) {
  return JSON.stringify(value, null, 2);
}

function formatCompactObjectLiteral(value) {
  return JSON.stringify(value);
}

function slugifyForPath(value) {
  const normalized = String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
  return normalized || "example";
}

function buildExampleSuggestedPath(builtinKey, exampleIndex) {
  const normalizedIndex = Number.isFinite(exampleIndex) && exampleIndex > 0 ? Math.floor(exampleIndex) : 1;
  return `/${slugifyForPath(builtinKey)}-${normalizedIndex}.m`;
}

function removeStaleGeneratedFiles(expectedNames) {
  if (!fs.existsSync(generatedBuiltinsDir)) {
    return;
  }
  for (const entry of fs.readdirSync(generatedBuiltinsDir, { withFileTypes: true })) {
    if (!entry.isFile() || !entry.name.endsWith(".ts")) {
      continue;
    }
    if (!expectedNames.has(entry.name)) {
      fs.rmSync(path.join(generatedBuiltinsDir, entry.name));
    }
  }
}

function main() {
  ensureDir(generatedBuiltinsDir);
  const sourceFiles = readBuiltinSourceFiles();
  const manifestEntries = [];
  const loaderEntries = [];
  const exampleCatalogEntries = [];
  const generatedNames = new Set();

  for (const fileName of sourceFiles) {
    const stem = path.basename(fileName, ".json");
    const key = normalizeBuiltinKey(stem);
    const sourcePath = path.join(builtinsDir, fileName);
    const parsed = JSON.parse(fs.readFileSync(sourcePath, "utf8"));
    const title = typeof parsed.title === "string" && parsed.title.trim() ? parsed.title : stem;
    const category = typeof parsed.category === "string" ? parsed.category : "";
    const categoryPath = categoryPathFromCategory(category);
    const slug = slugFromTitle(title);
    const keywords = Array.isArray(parsed.keywords)
      ? parsed.keywords.map((value) => String(value).trim()).filter(Boolean)
      : [];
    const summary = typeof parsed.summary === "string" ? parsed.summary : "";
    const doc = {
      ...parsed,
      key,
      title,
      slug,
      category,
      categoryPath,
      keywords,
      summary
    };
    const manifestEntry = {
      key,
      title,
      slug,
      category,
      categoryPath,
      keywords,
      summary,
      exampleCount: Array.isArray(parsed.examples) ? parsed.examples.length : 0
    };
    manifestEntries.push(manifestEntry);

    if (Array.isArray(parsed.examples)) {
      for (const [index, example] of parsed.examples.entries()) {
        const exampleTitle =
          typeof example.description === "string" && example.description.trim()
            ? example.description.trim()
            : `Example ${index + 1}`;
        const code = typeof example.input === "string" ? example.input : "";
        const output = typeof example.output === "string" ? example.output : undefined;
        exampleCatalogEntries.push({
          id: `${key}:${index + 1}`,
          builtinKey: key,
          builtinTitle: title,
          builtinSlug: slug,
          category,
          categoryPath,
          exampleTitle,
          summary,
          code,
          ...(output ? { output } : {}),
          keywords,
          suggestedPath: buildExampleSuggestedPath(key, index + 1),
        });
      }
    }

    const moduleFileName = `${stem}.ts`;
    generatedNames.add(moduleFileName);
    const modulePath = path.join(generatedBuiltinsDir, moduleFileName);
    const moduleContents = [
      "// @generated by scripts/generate-builtins.cjs",
      "// Do not edit by hand.",
      "",
      'import type { BuiltinDoc } from "../../builtins.js";',
      "",
      `const builtinDoc: BuiltinDoc = ${formatObjectLiteral(doc)};`,
      "",
      "export default builtinDoc;",
      ""
    ].join("\n");
    fs.writeFileSync(modulePath, moduleContents);
    loaderEntries.push(
      `  ${JSON.stringify(key)}: () => import(${JSON.stringify(`./builtins/${stem}.js`)}).then((mod) => mod.default),`
    );
  }

  removeStaleGeneratedFiles(generatedNames);

  const manifestContents = [
    "// @generated by scripts/generate-builtins.cjs",
    "// Do not edit by hand.",
    "",
    'import type { BuiltinDocLoader, BuiltinManifestEntry } from "../builtins.js";',
    "",
    `export const builtinManifest: BuiltinManifestEntry[] = ${formatObjectLiteral(manifestEntries)};`,
    "",
    "export const builtinDocLoaders: Record<string, BuiltinDocLoader> = {",
    ...loaderEntries,
    "};",
    "",
    "export const builtinExamplesCatalogLoader = () =>",
    '  import("./builtin-examples-catalog.js").then((mod) => mod.builtinExamplesCatalog);',
    ""
  ].join("\n");

  fs.writeFileSync(manifestPath, manifestContents);
  const examplesCatalogContents = [
    "// @generated by scripts/generate-builtins.cjs",
    "// Do not edit by hand.",
    "",
    'import type { BuiltinExampleCatalogEntry } from "../builtins.js";',
    "",
    `export const builtinExamplesCatalog: BuiltinExampleCatalogEntry[] = ${formatCompactObjectLiteral(exampleCatalogEntries)};`,
    ""
  ].join("\n");
  fs.writeFileSync(examplesCatalogPath, examplesCatalogContents);
  console.log(`Generated ${manifestEntries.length} builtin docs.`);
}

main();
